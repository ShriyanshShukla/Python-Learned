a = 'Harry'    # a is string
b = 345        # b is integer
c = 45.32      # c is float
d = True       # d is Boolean
e = None       # e is NoneType

# Python is Dynamic Programming language so it will know the datatype of variable by the value

print(a)
print(b)
print(c)
print(d)
print(e)

# Printing the type of variable

print(type(a))
print(type(e))
print(type(c))