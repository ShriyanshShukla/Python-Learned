age = 45

# It wil only print If both conditions are True
if age>34 and age<56:
    print("You can work with us")
else:
    print("You cannot work with us")

# It wil print even If only one conditions is True
if age>34 or age<56:
    print("You can work with us")
else:
    print("You cannot work with us")

# It will make True condition a False and vice versa
if not age>34:
    print("You can work with us")
else:
    print("You cannot work with us")